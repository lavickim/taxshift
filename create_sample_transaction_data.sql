-- 3. 테스트 케이스 통계 확인
SELECT 
    '=== 테스트 케이스 생성 완료 ===' as summary;

SELECT 
    expected_tag as 태그,
    COUNT(*) as 케이스수,
    ROUND(AVG(confidence_expected), 2) as 평균신뢰도,
    STRING_AGG(DISTINCT test_category, ', ') as 테스트카테고리
FROM sample_transactions 
GROUP BY expected_tag 
ORDER BY 케이스수 DESC;

SELECT 
    '총 테스트 케이스 수: ' || COUNT(*) as 총계,
    '평균 예상 신뢰도: ' || ROUND(AVG(confidence_expected), 3) as 평균신뢰도
FROM sample_transactions;

-- 4. 카테고리별 세부 현황
SELECT 
    CASE 
        WHEN test_category LIKE '%기본%' THEN '기본 케이스'
        WHEN test_category LIKE '%복합%' THEN '복합 케이스' 
        WHEN test_category LIKE '%경계%' THEN '경계 케이스'
        ELSE '일반 케이스'
    END as 케이스유형,
    COUNT(*) as 개수,
    ROUND(AVG(confidence_expected), 2) as 평균신뢰도
FROM sample_transactions 
GROUP BY 케이스유형
ORDER BY 케이스유형;