async function testJournalEntryValidation() {
  console.log('\n🔍 6. 분개 검증 로직 테스트');
  console.log('=' .repeat(50));
  
  // 테스트용 분개 데이터 (클라이언트 사이드 검증)
  const testJournalEntry = {
    id: 999,
    companyId: TEST_COMPANY_ID,
    entryDate: '2025-07-22',
    description: '테스트 분개',
    totalAmount: 100000,
    details: [
      {
        id: 1,
        lineNumber: 1,
        accountCode: '5101',
        accountName: '접대비',
        debitAmount: 100000,
        creditAmount: 0,
        description: '고객 접대비'
      },
      {
        id: 2,
        lineNumber: 2,
        accountCode: '1120',
        accountName: '보통예금',
        debitAmount: 0,
        creditAmount: 100000,
        description: '현금 지출'
      }
    ]
  };
  
  console.log('📤 테스트 분개:', JSON.stringify({
    총금액: testJournalEntry.totalAmount,
    라인수: testJournalEntry.details.length,
    차변합계: testJournalEntry.details.reduce((sum, d) => sum + d.debitAmount, 0),
    대변합계: testJournalEntry.details.reduce((sum, d) => sum + d.creditAmount, 0)
  }, null, 2));
  
  // 클라이언트 사이드 검증 로직 (BookkeepingService.validateJournalEntry와 동일)
  function validateJournalEntry(journalEntry) {
    const errors = [];
    
    if (!journalEntry.details || journalEntry.details.length === 0) {
      errors.push('분개 상세가 없습니다.');
      return { isBalanced: false, totalDebit: 0, totalCredit: 0, errors };
    }

    if (journalEntry.details.length < 2) {
      errors.push('분개는 최소 2개 라인이 필요합니다.');
    }

    const totalDebit = journalEntry.details.reduce((sum, detail) => sum + detail.debitAmount, 0);
    const totalCredit = journalEntry.details.reduce((sum, detail) => sum + detail.creditAmount, 0);

    const isBalanced = totalDebit === totalCredit;
    
    if (!isBalanced) {
      errors.push(`차변 합계(₩${totalDebit.toLocaleString()})와 대변 합계(₩${totalCredit.toLocaleString()})가 일치하지 않습니다.`);
    }

    if (totalDebit !== journalEntry.totalAmount) {
      errors.push(`분개 총액(₩${journalEntry.totalAmount.toLocaleString()})과 차변 합계(₩${totalDebit.toLocaleString()})가 일치하지 않습니다.`);
    }

    journalEntry.details.forEach((detail, index) => {
      const hasDebit = detail.debitAmount > 0;
      const hasCredit = detail.creditAmount > 0;

      if (hasDebit && hasCredit) {
        errors.push(`라인 ${index + 1}: 차변과 대변 금액이 동시에 있을 수 없습니다.`);
      }

      if (!hasDebit && !hasCredit) {
        errors.push(`라인 ${index + 1}: 차변 또는 대변 중 하나의 금액이 있어야 합니다.`);
      }
    });

    return {
      isBalanced: isBalanced && errors.length === 0,
      totalDebit,
      totalCredit,
      errors
    };
  }
  
  const validationResult = validateJournalEntry(testJournalEntry);
  
  console.log('📋 검증 결과:', validationResult.isBalanced ? '✅ 유효' : '❌ 무효');
  console.log('📋 차변 합계:', validationResult.totalDebit.toLocaleString(), '원');
  console.log('📋 대변 합계:', validationResult.totalCredit.toLocaleString(), '원');
  
  if (validationResult.errors.length > 0) {
    console.log('📋 오류 목록:');
    validationResult.errors.forEach((error, index) => {
      console.log(`  ${index + 1}. ${error}`);
    });
  }
  
  return validationResult;
}

// 메인 테스트 실행
async function runAllTests() {
  console.log('🧪 MoneyShift 모바일 앱 복식부기 기능 테스트 시작');
  console.log('='.repeat(80));
  console.log('📅 테스트 시간:', new Date().toLocaleString('ko-KR'));
  console.log('🌐 API 서버:', API_BASE_URL);
  
  let testResults = {
    total: 0,
    passed: 0,
    failed: 0
  };
  
  const tests = [
    { name: '헬스 체크', func: testHealthCheck },
    { name: '거래 분개 변환', func: testProcessTransaction },
    { name: '분개 목록 조회', func: testGetJournalEntries },
    { name: '계정과목 조회', func: testGetChartOfAccounts },
    { name: '클라이언트 검증', func: testJournalEntryValidation }
  ];
  
  let journalEntryId = null;
  
  for (const test of tests) {
    testResults.total++;
    
    try {
      let result;
      if (test.name === '거래 분개 변환') {
        result = await test.func();
        journalEntryId = result; // 분개 ID 저장
      } else {
        result = await test.func(journalEntryId);
      }
      
      if (result !== null && result !== false) {
        testResults.passed++;
        console.log(`\n✅ ${test.name} 테스트 통과`);
      } else {
        testResults.failed++;
        console.log(`\n❌ ${test.name} 테스트 실패`);
      }
    } catch (error) {
      testResults.failed++;
      console.log(`\n💥 ${test.name} 테스트 중 예외 발생:`, error.message);
    }
  }
  
  // 분개 상세 조회 테스트 (journalEntryId가 있는 경우에만)
  if (journalEntryId) {
    testResults.total++;
    try {
      const result = await testGetJournalEntry(journalEntryId);
      if (result) {
        testResults.passed++;
        console.log(`\n✅ 분개 상세 조회 테스트 통과`);
      } else {
        testResults.failed++;
        console.log(`\n❌ 분개 상세 조회 테스트 실패`);
      }
    } catch (error) {
      testResults.failed++;
      console.log(`\n💥 분개 상세 조회 테스트 중 예외 발생:`, error.message);
    }
  }
  
  // 테스트 결과 요약
  console.log('\n' + '='.repeat(80));
  console.log('📊 테스트 결과 요약');
  console.log('='.repeat(80));
  console.log('🔢 총 테스트:', testResults.total);
  console.log('✅ 통과:', testResults.passed);
  console.log('❌ 실패:', testResults.failed);
  console.log('📈 성공률:', Math.round((testResults.passed / testResults.total) * 100), '%');
  
  if (testResults.failed === 0) {
    console.log('\n🎉 모든 테스트가 통과했습니다! 모바일 앱 복식부기 기능이 정상적으로 구현되었습니다.');
  } else {
    console.log('\n⚠️  일부 테스트가 실패했습니다. 백엔드 서버나 API 엔드포인트를 확인해주세요.');
  }
  
  console.log('\n📋 다음 단계:');
  console.log('  1. 모바일 앱에서 실제 화면을 통한 사용자 테스트');
  console.log('  2. Redux 상태 관리 통합 테스트');
  console.log('  3. 네트워크 오류 시나리오 테스트');
  console.log('  4. 대용량 데이터 처리 성능 테스트');
  
  process.exit(testResults.failed === 0 ? 0 : 1);
}

// fetch API 폴리필 (Node.js 환경)
async function setupFetch() {
  if (typeof fetch === 'undefined') {
    const { default: fetch } = await import('node-fetch');
    global.fetch = fetch;
  }
}

// 테스트 실행
setupFetch().then(() => {
  return runAllTests();
}).catch(error => {
  console.error('💥 테스트 실행 중 치명적 오류:', error);
  process.exit(1);
});